public class HideAndSeek {
    public static void main(String[] args) {
        // println makes a new line for every string / statements
        System.out.println("Let's play Hide and Seek!");
        // print continous on the same line, and remembers where the cursor was.
        System.out.print("Three...");
        System.out.print("Two...");
        // apparently the println continous on the same line where the print statment stopped...
        System.out.println("One...");
        System.out.println("Ready or not, here I come!");
    }
}
